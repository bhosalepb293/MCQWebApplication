package Model;

public class DTO {
	private int book_id;
	private String book_name;
	private String book_auth;
	private String no_of_pages;
	
	public int getBook_id() {
		return book_id;
	}
	public void setBook_id(int book_id) {
		this.book_id = book_id;
	}
	public String getBook_name() {
		return book_name;
	}
	public void setBook_name(String book_name) {
		this.book_name = book_name;
	}
	public String getBook_auth() {
		return book_auth;
	}
	public void setBook_auth(String book_auth) {
		this.book_auth = book_auth;
	}
	public String getNo_of_pages() {
		return no_of_pages;
	}
	public void setNo_of_pages(String no_of_pages) {
		this.no_of_pages = no_of_pages;
	}

}
